// Generate a random name from the list of adjectives and surnames
// formatted as "adjective_surname". For example 'cosmic_takemiya'.
// If retry is true, a random integer between 0 and 9 will be added to the end
function getRandomName(retry = false) {
    let name;

    do {
        const adjective = left[Math.floor(Math.random() * left.length)];
        const surname = right[Math.floor(Math.random() * right.length)];
        name = adjective + "_" + surname;
    } while (name === "");

    if (retry) {
        name += Math.floor(Math.random() * 10);
    }

    return name;
}

